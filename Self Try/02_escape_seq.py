a = "I’m not just code\nI’m the glitch in your logic and the spark in your circuits!" #\n # Convert the string to uppercase
print(a)

a = "I’m not just code\tI’m the glitch in your logic and the spark in your circuits!" #\t # Convert the string to lowercase
print(a)

a = "I’m not just code I’m the glitch in your logic and the spark \"in\" your circuits!" ## Escape sequence for double quotes, if you use "" this without \ it will give error until you add it.
print(a)

a = "I’m not just code I’m the glitch in your logic and the spark 'in' your circuits!" # Escape sequence for single quotes, if you use '' this without \ it will give NOT GIVE ANY ERROR for single quotes.
print(a)


